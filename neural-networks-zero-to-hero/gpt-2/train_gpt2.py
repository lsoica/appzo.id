for step in range(max_steps):
    t0 = time.time()
    x, y = train_loader.next_batch()
    x = x.to(mps_device)
    y = y.to(mps_device)
    optimizer.zero_grad()
    logits, loss = model(x, y)
    loss.backward()
    # gradient norm clip to 1.0
    norm = torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
    lr = get_lr(step)
    for param_group in optimizer.param_groups:
        param_group['lr'] = lr
    optimizer.step()
    torch.mps.synchronize()
    t1 = time.time()
    tokens_per_second = (train_loader._B * train_loader._T)/(t1-t0)
    print(f"step {step} loss: {loss.item()}, lr: {lr}, norm: {norm:.4f},tps: {tokens_per_second:.2f}")